package org.intracode.heliot;

public class XyValuesPlant2 {

    private double x_plant2;
    private double y_plant2;

    public XyValuesPlant2(double x_plant2, double y_plant2) {
        this.x_plant2 = x_plant2;
        this.y_plant2 = y_plant2;
    }

    public double getX_plant2() {
        return x_plant2;
    }

    public void setX_plant2(double x_plant2) {
        this.x_plant2 = x_plant2;
    }

    public double getY_plant2() {
        return y_plant2;
    }

    public void setY_plant2(double y_plant2) {
        this.y_plant2 = y_plant2;
    }
}
