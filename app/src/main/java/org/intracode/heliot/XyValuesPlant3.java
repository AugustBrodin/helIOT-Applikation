package org.intracode.heliot;

public class XyValuesPlant3 {

    private double x_plant3;
    private double y_plant3;

    public XyValuesPlant3(double x_plant3, double y_plant3) {
        this.x_plant3 = x_plant3;
        this.y_plant3 = y_plant3;
    }

    public double getX_plant3() {
        return x_plant3;
    }

    public void setX_plant3(double x_plant3) {
        this.x_plant3 = x_plant3;
    }

    public double getY_plant3() {
        return y_plant3;
    }

    public void setY_plant3(double y_plant3) {
        this.y_plant3 = y_plant3;
    }
}
